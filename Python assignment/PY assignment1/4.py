str = input("Enter a string: ")
len = len(str)

print("The length of the string is:", len)

first_char = str[0]
last_char = str[-1]
print("The first character of the str is:", first_char)
print("The last character of the str is:", last_char)

reverse_str = str[::-1]
print("The string in reverse order is:", reverse_str)
